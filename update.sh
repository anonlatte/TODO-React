#!/bin/bash

# Скрипт для обновления всех пакетов и устранения уязвимостей

echo "Удаляем старые зависимости..."
rm -rf node_modules package-lock.json

echo "Обновляем все пакеты до последних версий..."
npx npm-check-updates -u

echo "Переустанавливаем зависимости..."
npm install

echo "Автоматически исправляем уязвимости..."
npm audit fix

echo "Принудительное исправление уязвимостей (если необходимо)..."
npm audit fix --force

echo "Проверяем состояние после обновления:"
npm audit

echo "Готово! Пожалуйста, протестируйте приложение, чтобы убедиться, что все работает корректно."
